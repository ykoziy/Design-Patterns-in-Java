public class DataClass {
    private int num1;
    private int num2;
    private int num3;
    
    public DataClass(int num1, int num2, int num3) {
        this.num1 = num1;
        this.num2 = num2;
        this.num3 = num3;
    }
    
    public int getNum1() {
        return num1;
    }
    
    public int getNum2() {
        return num2;
    }
    
    public int getNum3() {
        return num3;
    }    
}
